    private void approveApplicant() {
    String idNumber = jTextPane1.getText();
    
    if (idNumber.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please search for an applicant first!", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/hcas", "root", "Wi0797563115#")) {
        // Retrieve the cover limit from the database
        String query = "SELECT Amount FROM cover WHERE Cover = ?";
        PreparedStatement pstmt = conn.prepareStatement(query);
        pstmt.setString(1, jLabel15.getText().trim()); // Cover type from the label
        ResultSet rs = pstmt.executeQuery();

        if (rs.next()) {
            double coverLimit = rs.getDouble("Amount");
            double applicantAmount = Double.parseDouble(jLabel16.getText().trim()); // Amount from the label

            String approvalMessage = "";
            boolean approved = false; // Track approval status
            double finalApprovedAmount = applicantAmount; // Final amount to be approved

            // Decision logic
            if (applicantAmount > coverLimit) {
                approvalMessage = "We will allocate the amount that matches your cover limit.\n"
                        + "Allocated amount: " + coverLimit + "\n"
                        + "Requested amount: " + applicantAmount;
                finalApprovedAmount = coverLimit; // Allocate only the maximum cover limit
                approved = true;
            } 
            else if (applicantAmount < coverLimit) {
                approvalMessage = "Approval Successful!\nApproved amount: " + applicantAmount +
                        "\nWithin cover limit: " + coverLimit;
                approved = true;
            } else { // When applicantAmount == coverLimit
                approvalMessage = "Approval Successful!\nApproved amount: " + applicantAmount +
                        "\nMatches cover limit: " + coverLimit;
                approved = true;
            }

            JOptionPane.showMessageDialog(this, approvalMessage, "Success", JOptionPane.INFORMATION_MESSAGE);

            // If approved, update the status in the normal table
            if (approved) {
                String updateQuery = "UPDATE normal SET approval_status = 'Approved', Amount_Approved = ? WHERE Id_no = ?";
                PreparedStatement updateStmt = conn.prepareStatement(updateQuery);
                updateStmt.setDouble(1, finalApprovedAmount); // Store the approved amount in the database
                updateStmt.setString(2, idNumber);
                int updatedRows = updateStmt.executeUpdate();
                
                if (updatedRows > 0) {
                    JOptionPane.showMessageDialog(this, "Status updated to Approved!\nFinal Allocated Amount: " + finalApprovedAmount, "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update status!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cover type not found in database!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
      
    }
    
    
